def get_cats_info(path):
    cats = []
    try:
        with open(path, encoding="utf-8") as file:
            for line in file:
                cat_id, name, age = line.strip().split(",")
                cats.append({
                    "id": cat_id,
                    "name": name,
                    "age": age
                })
    except FileNotFoundError:
        print("Файл не найдено")
        return []
    except ValueError:
        print("Файл содержит некорректные данные")
        return []
    return cats
